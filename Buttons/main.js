function button1MouseDown(){
	document.getElementById("firstButton").style.boxShadow = "inset 0px 0px 8px #666666";
}

function button1MouseUp(){
	document.getElementById("firstButton").style.boxShadow = "0px 0px 8px #2eb82e";
	document.getElementById("firstButton").style.border = "3px solid #2eb82e"
}